package pojo;

public class Buckets {
	
	private boolean is_catch_all;
	private String name;
	private int percentage;
	private int ad_provider;
	private int keyword_list;
	private String device;
	private String is_payable_to_publisher;
	private int template;


	public boolean isIs_catch_all() {
		return is_catch_all;
	}
	public void setIs_catch_all(boolean is_catch_all) {
		this.is_catch_all = is_catch_all;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPercentage() {
		return percentage;
	}
	public void setPercentage(int percentage) {
		this.percentage = percentage;
	}
	public int getAd_provider() {
		return ad_provider;
	}
	public void setAd_provider(int ad_provider) {
		this.ad_provider = ad_provider;
	}
	public int getKeyword_list() {
		return keyword_list;
	}
	public void setKeyword_list(int keyword_list) {
		this.keyword_list = keyword_list;
	}
	public String getDevice() {
		return device;
	}
	public void setDevice(String device) {
		this.device = device;
	}
	public String getIs_payable_to_publisher() {
		return is_payable_to_publisher;
	}
	public void setIs_payable_to_publisher(String is_payable_to_publisher) {
		this.is_payable_to_publisher = is_payable_to_publisher;
	}
	public int getTemplate() {
		return template;
	}
	public void setTemplate(int template) {
		this.template = template;
	}


}
